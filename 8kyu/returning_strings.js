// My solution
function greet(name) {
  return `Hello, ${name} how are you doing today?`;
}

// Trials
console.log(greet("Oscar")); //"Hello, Oscar how are you doing today?"
console.log(greet("Mike")); //"Hello, Mike how are you doing today?"
console.log(greet("Sarah")); //"Hello, Sarah how are you doing today?"
